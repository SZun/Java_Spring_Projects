package com.sg.vendingmachine.vendingmachine.dtos;

import java.math.BigDecimal;

/**
 *
 * @author samg.zun
 */
public class Item {
    private String name;
    private BigDecimal cost;
    private int total;
    
    public Item(){}

    public Item(String name, BigDecimal cost, int total) {
        this.name = name;
        this.cost = cost;
        this.total = total;
    }
    
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the cost
     */
    public BigDecimal getCost() {
        return cost;
    }

    /**
     * @param cost the cost to set
     */
    public void setCost(BigDecimal cost) {
        this.cost = cost;
    }

    /**
     * @return the total
     */
    public int getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(int total) {
        this.total = total;
    }
    
    
}
